# checking the weekdays using if else
option=int(input("enter the day number to check:"))
if option==1:
    print("It is sunday")
elif option==2:
    print("It is monday")
elif option==3:
    print("It is tuesday")
elif option==4:
    print("It is wednesday")
elif option==5:
    print("It is thursday")
elif option==6:
    print("It is friday")
elif option==7:
    print("It is saturday")
else:
    print("enter the valid num")
